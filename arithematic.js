// ARITHEMATIC OPERATORS

let a = 12;
let b = 5;

console.log("a + b = ", a + b);
console.log("a - b = ", a - b);
console.log("a * b = ", a * b);
console.log("a % b = ", a % b);
console.log("a ** b = ", a ** b);
console.log("a++ = ", a++);
console.log("a-- = ", a--);
console.log("++a = ", ++a);
console.log("--a = ", --a);
